/*
Esercizio P1.4 - Scrivete un programma StaircasePrinter che visualizzi una scala:
  															+---+
																|   |
														+---+---+
														|   |   |
												+---+---+---+
												|   |   |   |
										+---+---+---+---+
										|   |   |   |   |
										+---+---+---+---+
 */
public class StaircasePrinter
{

	public static void main(String[] args)
	{

		System.out.println("            +---+");
		System.out.println("            |   |");
		System.out.println("        +---+---+");
		System.out.println("        |   |   |");
		System.out.println("    +---+---+---+");
		System.out.println("    |   |   |   |");
		System.out.println("+---+---+---+---+");
		System.out.println("|   |   |   |   |");
		System.out.println("+---+---+---+---+");

		System.exit(0);

	}

}
